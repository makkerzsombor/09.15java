package hu.petrik.pontoop.kor;

public class Kor {
    private int r;
    private int x;
    private int y;


    public Kor(int r){
        this.r = r;
        this.x = 0;
        this.y = 0;
    }
    public Kor(int r, int x, int y){
        this.r = r;
        this.x = x;
        this.y = y;
    }


}
